package com.example.praktikum2;

public class ClassNama {
    private String Nama;

    //inisialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }
    //membuat metod getName untuk mengembalikan nilai ke variable Nama
    public String getNama()
    {
        return this.Nama;
    }
}

